    Tipo VARCHAR(20) NOT NULL CHECK (Tipo IN ('MEDICA', 'ODONTOLOGICA', 'FARMACEUTICA', 'BIOQUIMICA')),
    
    /** Descripcion de la especialidad */
    Texto VARCHAR(250) NOT NULL,

    Vigencia BIT NOT NULL DEFAULT 1,

    [CreatedBy] [varchar](10) NOT NULL DEFAULT (suser_name()) ,
	[CreatedAt] [datetime] NOT NULL DEFAULT (getdate()),

    CONSTRAINT UQ_DEIS_ESPECIALIDAD_CODIGO_TIPO UNIQUE (Codigo, Tipo),
);

