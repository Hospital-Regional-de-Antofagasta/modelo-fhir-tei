CREATE TABLE [dbo].[TAB_DEIS_Especialidad] (
    /** El identificador interno de la especialidad */
    Id INTEGER CONSTRAINT PK_DEIS_ESPECIALIDAD PRIMARY KEY,

    /** Codigo de la especialidad según la norma 820 (https://archive.org/details/norma-tecnica-820-estandares-de-informacion-de-salud-feb-2023) */
